integer = 10
print(integer)
print(type(integer))

# integer ke float 
int_to_float = float(integer) #casting
print(int_to_float)
print(type(int_to_float))

# float ke int
desimal = 12.5
print(desimal)
des_to_int = int(desimal) #casting
print(des_to_int)

# int ke string
bilangan = 15
print(bilangan)
print(type(bilangan))

int_to_string = str(bilangan) #casting
print(int_to_string)
print(type(int_to_string))